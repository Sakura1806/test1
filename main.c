#include <stdio.h>

int main() {
    int a;

    // 输入年龄值
    printf("请输入年龄值:");
    scanf("%d", &a);

    // 计算表达式：a += a *= a /= a - 5
    // 运算顺序从右向左：先算a /= a-5，再算a *= 结果，最后算a += 结果
    a += a *= a /= a - 5;

    // 输出结果
    printf("结果是%d\n", a);

    return 0;
}
